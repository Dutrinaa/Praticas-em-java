package b2_exercicios_05;

public class Matematica {

	public int somaDoisInt(int a, int b) {
		return a + b;
	}

	public int somaTresInt(int a, int b, int c) {
		return a + b + c;
	}

	public int somaDoisString(String a, String b) {
		return Integer.parseInt(a) + Integer.parseInt(b);
	}
}
